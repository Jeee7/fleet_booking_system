class Vehicle {
  final String name;
  final String type;
  final String image;
  final String location;
  final int price;
  final double latitude;
  final double longitude;
  final String licensePlate;

  Vehicle({
    required this.name,
    required this.type,
    required this.image,
    required this.location,
    required this.price,
    required this.latitude,
    required this.longitude,
    required this.licensePlate,
  });
}
